public class RelationalOperators {
    public static void main(String[] args) {
        int p = 4, q = 2;
        System.out.println(p==q);
        System.out.println(p<q);
        System.out.println(p>q);
        System.out.println(p<=q);
        System.out.println(p>=q);
        System.out.println(p!=q);
    }
}
